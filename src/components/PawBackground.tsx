const SCALE = 0.58;
const TILE_W = 170;
const TILE_H = 145;
const PAIR_DX = 30;
const PAIR_DY = 28;

export function PawBackground({ width, height }: { width: number; height: number }) {
  const cols = Math.ceil(width / TILE_W) + 2;
  const rows = Math.ceil(height / TILE_H) + 2;

  const groups: { x: number; y: number }[] = [];
  for (let row = -1; row <= rows; row++) {
    for (let col = -1; col <= cols; col++) {
      const xOffset = row % 2 !== 0 ? TILE_W / 2 : 0;
      groups.push({ x: col * TILE_W + xOffset, y: row * TILE_H });
    }
  }

  return (
    <Svg width={width} height={height}>
      {groups.map((pos, i) => (
        <G key={i} opacity={0.6}>
          {/* First paw of the pair */}
          <G transform={`translate(${pos.x}, ${pos.y}) scale(${SCALE})`}>
            <Path d={PAW_D} fill="#D9CF88" />
          </G>
          {/* Second paw slightly offset */}
          <G transform={`translate(${pos.x + PAIR_DX}, ${pos.y + PAIR_DY}) scale(${SCALE})`}>
            <Path d={PAW_D} fill="#D9CF88" />
          </G>
        </G>
      ))}
    </Svg>
  );
}
